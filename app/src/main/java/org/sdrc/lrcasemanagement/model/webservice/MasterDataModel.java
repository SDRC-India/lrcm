package org.sdrc.lrcasemanagement.model.webservice;

import java.util.List;

/**
 * 
 *
 * @author Ratikanta Pradhan (ratikanta@sdrc.co.in) on 28-Jan-2017 4:28:03 pm
 */
public class MasterDataModel {

	private UserModel userModel;
	private List<AreaModel> areaModels;
	private List<TypeModel> typeModels;
	private List<AreaLevelModel> areaLevelModel;
	private int latestPatientNumber;
	private LatestSerialNoOfMonthsModel latestSerialNoOfMonthsModel;
	private String errorMessage;
	private List<PatientModel> patientModels;
	public UserModel getUserModel() {
		return userModel;
	}
	public void setUserModel(UserModel userModel) {
		this.userModel = userModel;
	}
	public List<AreaModel> getAreaModels() {
		return areaModels;
	}
	public void setAreaModels(List<AreaModel> areaModels) {
		this.areaModels = areaModels;
	}
	public List<TypeModel> getTypeModels() {
		return typeModels;
	}
	public void setTypeModels(List<TypeModel> typeModels) {
		this.typeModels = typeModels;
	}
	public List<AreaLevelModel> getAreaLevelModel() {
		return areaLevelModel;
	}
	public void setAreaLevelModel(List<AreaLevelModel> areaLevelModel) {
		this.areaLevelModel = areaLevelModel;
	}
	public int getLatestPatientNumber() {
		return latestPatientNumber;
	}
	public void setLatestPatientNumber(int latestPatientNumber) {
		this.latestPatientNumber = latestPatientNumber;
	}
	public LatestSerialNoOfMonthsModel getLatestSerialNoOfMonthsModel() {
		return latestSerialNoOfMonthsModel;
	}
	public void setLatestSerialNoOfMonthsModel(
			LatestSerialNoOfMonthsModel latestSerialNoOfMonthsModel) {
		this.latestSerialNoOfMonthsModel = latestSerialNoOfMonthsModel;
	}
	public String getErrorMessage() {
		return errorMessage;
	}
	public void setErrorMessage(String errorMessage) {
		this.errorMessage = errorMessage;
	}
	public List<PatientModel> getPatientModels() {
		return patientModels;
	}
	public void setPatientModels(List<PatientModel> patientModels) {
		this.patientModels = patientModels;
	}
	
}
