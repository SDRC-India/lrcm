package org.sdrc.lrcasemanagement.model;

import java.io.Serializable;
import java.util.Date;

/**
 * Created by Ratikanta Pradhan (ratikanta@sdrc.co.in) on 26-01-2017.
 */

public class DataModel implements Serializable{

    private String patientId;
    private String patientName;
    private int patientAge;
    private String mctsNo;
    private Date admissionDate;
    private boolean isPatientHadPreInjection;
    private boolean isPatientHadPostInjection;


    public String getPatientId() {
        return patientId;
    }

    public void setPatientId(String patientId) {
        this.patientId = patientId;
    }

    public String getPatientName() {
        return patientName;
    }

    public void setPatientName(String patientName) {
        this.patientName = patientName;
    }

    public int getPatientAge() {
        return patientAge;
    }

    public void setPatientAge(int patientAge) {
        this.patientAge = patientAge;
    }

    public String getMctsNo() {
        return mctsNo;
    }

    public void setMctsNo(String mctsNo) {
        this.mctsNo = mctsNo;
    }

    public Date getAdmissionDate() {
        return admissionDate;
    }

    public void setAdmissionDate(Date admissionDate) {
        this.admissionDate = admissionDate;
    }

    public boolean isPatientHadPreInjection() {
        return isPatientHadPreInjection;
    }

    public void setPatientHadPreInjection(boolean patientHadPreInjection) {
        isPatientHadPreInjection = patientHadPreInjection;
    }

    public boolean isPatientHadPostInjection() {
        return isPatientHadPostInjection;
    }

    public void setPatientHadPostInjection(boolean patientHadPostInjection) {
        isPatientHadPostInjection = patientHadPostInjection;
    }
}
